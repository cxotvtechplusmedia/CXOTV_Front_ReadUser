/**
 * Convert slug to category name using complete mapping
 */
export function slugToCategoryNameComplete(slug) {
  if (!slug) return "";

  const normalizedSlug = slug.toLowerCase().trim();

  // Check complete mapping first
  if (COMPLETE_CATEGORY_MAPPINGS[normalizedSlug]) {
    return COMPLETE_CATEGORY_MAPPINGS[normalizedSlug];
  }

  // Fallback: capitalize each word
  return normalizedSlug
    .split("-")
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join(" ");
}

/**
 * Get all possible slugs for a category name (case-insensitive search)
 */
export function categoryNameToSlugComplete(name) {
  if (!name) return "";

  const normalizedName = name.toLowerCase().trim();

  // Search in complete mappings
  for (const [slug, categoryName] of Object.entries(COMPLETE_CATEGORY_MAPPINGS)) {
    if (categoryName.toLowerCase() === normalizedName) {
      return slug;
    }
  }

  // Fallback: convert to slug format
  return normalizedName
    .replace(/\s+/g, "-")
    .replace(/[^a-z0-9-]/g, "");
}
